package com.example.bean;
/**
 * 人物实体类
 * @author Simon
 *
 */
public class Person {
	
	private int pid; // 人物id
	private String name; // 人物名字

	public int getPid() {
		return pid;
	}

	public void setPid(int pid) {
		this.pid = pid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
